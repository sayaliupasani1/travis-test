def add(num1, num2, num3):
	"""
	Function to add three numbers
	Parameters
	----------
	num1: int, float
	num2: int, float
	num3: int, float
	"""
	return num1 + num2 + num3

def sub(num1, num2):
	"""
	Function to subtract two numbers:
	Parameters
	----------
	num1: int, float
	num2: int, float
	"""
	return num1 - num2

def multiply(num1, num2):
	"""
	Function to multiply two numbers
	Parameters
	----------
	num1: int, float
	num2: int, float
	"""
	return num1 * num2

def divide(num1, num2):
	"""
	Function to divide two numbers
	Parameters
	----------
	num1: int, float
	num2: int, float
	"""
	return num1/num2
